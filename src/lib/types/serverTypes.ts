
/**
 * Типы данных, связанные с сервером
 */

/**
 * Интерфейс для статистики сервера
 */
export interface ServerStat {
  /** Название статистического показателя */
  name: string;
  /** Значение показателя (строковое или числовое) */
  value: string | number;
  /** Дополнительное описание показателя */
  description?: string;
  /** Изменение показателя в сравнении с предыдущим периодом */
  change?: number;
  /** Тренд изменения: вверх/вниз/нейтральный */
  trend?: 'up' | 'down' | 'neutral';
}
